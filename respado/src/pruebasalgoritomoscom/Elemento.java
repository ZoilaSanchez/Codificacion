package pruebasalgoritomoscom;

/**
 *
 * @author Lopez
 */
public class Elemento {
    private char x;
    private String z;

    public Elemento(char x, String z) {
        this.x = x;
        this.z = z;
    }

    public char getX() {
        return x;
    }

    public void setX(char x) {
        this.x = x;
    }

    public String getZ() {
        return z;
    }

    public void setZ(String z) {
        this.z = z;
    }
    
}
